helpers do

  def icons
    File.readlines('icons.txt')
  end

  def button_colors
    ["", "blue", "red", "purple", "yellow", "green", "black", "gray"]
  end

  def random_string(length=10)
    chars = 'abcdefghjkmnpqrstuvwxyzABCDEFGHJKLMNPQRSTUVWXYZ23456789'
    password = ''
    length.times { password << chars[rand(chars.size)] }
    password
  end

  def nav_active(options = {})
    root_path = "pages/"
    current_route = request.path.gsub(root_path, "").split("/") # pages/dashboard/stats.html -> ['dashboard', 'stats.html']
    @primary = current_route.first # dashboard
    @secondary = current_route.last.split(".").first #stats

    if options[:primary]
      #activate link if the controller is options[:controller]
      return "active" if options[:primary] == @primary
    end

    if options[:secondary]
      return "active" if options[:secondary] == @secondary
    end
  end
end

